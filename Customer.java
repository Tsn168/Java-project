public class Customer {
    String userID;
    String fullName;
    String username;
    String password;
    String phone;
    String email;

    // Constructor
    public Customer(String userID, String fullName, String username, String password, String phone, String email) {
        this.userID = userID;
        this.fullName = fullName;
        this.username = username;
        this.password = password;
        this.phone = phone;
        this.email = email;
    }
}
